  function getPath(obj, path) {
    var parts = path.split('.');
    var cur = obj;
    for (var i = 0; i < parts.length; i++) {
      if (cur == null) return undefined;
      cur = cur[parts[i]];
    }
    return cur;
  }

  function t(key, params) {
    var dict = window.PCC_I18N[window.pccCurrentLang] || window.PCC_I18N.en;
    var str = getPath(dict, key);
    if (str === undefined) str = getPath(window.PCC_I18N.en, key);
    if (str === undefined) return key;
    if (params) {
      str = str.replace(/\{(\w+)\}/g, function (m, k) {
        return (params[k] !== undefined && params[k] !== null) ? params[k] : '';
      });
    }
    return str;
  }
  window.t = t;

  function applyLanguage(langCode) {
    if (!window.PCC_I18N[langCode]) langCode = 'en';
    window.pccCurrentLang = langCode;
    localStorage.setItem(STORAGE_KEY, langCode);
    document.documentElement.lang = langCode;
    document.documentElement.dir = RTL_LANGS.indexOf(langCode) !== -1 ? 'rtl' : 'ltr';

    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      el.textContent = t(el.getAttribute('data-i18n'));
    });
    document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
      el.innerHTML = t(el.getAttribute('data-i18n-html'));
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
      el.setAttribute('placeholder', t(el.getAttribute('data-i18n-placeholder')));
    });
    document.querySelectorAll('[data-i18n-title]').forEach(function (el) {
      el.setAttribute('title', t(el.getAttribute('data-i18n-title')));
    });
    document.querySelectorAll('[data-i18n-aria-label]').forEach(function (el) {
      el.setAttribute('aria-label', t(el.getAttribute('data-i18n-aria-label')));
    });

    document.querySelectorAll('.pcc-lang-current').forEach(function (el) {
      el.textContent = langCode.toUpperCase();
    });

    document.querySelectorAll('.pcc-lang-dropdown a, .pcc-lang-flat a').forEach(function (a) {
      if (a.getAttribute('data-lang') === langCode) a.classList.add('active');
      else a.classList.remove('active');
    });

    window.dispatchEvent(new CustomEvent('pcc:langchange', { detail: langCode }));
  }
  window.applyLanguage = applyLanguage;

  // Supports multiple simultaneous switcher instances on one page (e.g. owner.html's
  // pre-login panel + post-login header) — everything is scoped via the clicked
  // button's own .pcc-lang-wrap ancestor rather than a page-wide id.
  function pccToggleLangMenu(btn) {
    var wrap = btn.closest('.pcc-lang-wrap');
    var dropdown = wrap && wrap.querySelector('.pcc-lang-dropdown');
    if (!dropdown) return;
    dropdown.classList.toggle('open');
    if (dropdown.classList.contains('open')) {
      var closeHandler = function (e) {
        if (!wrap.contains(e.target)) {
          dropdown.classList.remove('open');
          document.removeEventListener('click', closeHandler);
        }
      };
      setTimeout(function () { document.addEventListener('click', closeHandler); }, 0);
    }
  }
  window.pccToggleLangMenu = pccToggleLangMenu;


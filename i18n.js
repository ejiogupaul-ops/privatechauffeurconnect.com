  function buildLinks() {
    return LANGS.map(function (l) {
      return '<a href="#" data-lang="' + l + '" onclick="applyLanguage(\'' + l + '\'); return false;">' + LANG_NAMES[l] + '</a>';
    }).join('');
  }

  function buildDropdownHTML() {
    return '<div class="pcc-lang-wrap">' +
      '<button type="button" class="pcc-lang-btn" onclick="pccToggleLangMenu(this)">🌐 <span class="pcc-lang-current">EN</span> <span class="pcc-lang-arrow">▾</span></button>' +
      '<div class="pcc-lang-dropdown">' + buildLinks() + '</div>' +
      '</div>';
  }

  function buildFlatHTML() {
    return '<div class="pcc-lang-flat">' + buildLinks() + '</div>';
  }

  window.pccRenderSwitcher = function (mountSelector, variant) {
    injectSwitcherStyles();
    var mount = document.querySelector(mountSelector);
    if (!mount) return;
    mount.innerHTML = (variant === 'flat') ? buildFlatHTML() : buildDropdownHTML();
  };

  function init() {
    var saved = localStorage.getItem(STORAGE_KEY) || 'en';
    applyLanguage(saved);
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', init);
  } else {
    init();
  }
